print("Задача 2: Обратный отсчет до запуска")
number = int(input("Введите число для обратного отсчета: "))      #ввод данных
print("Обратный отсчет:")
while number > 0:                                        #даем условие циклу:если номер больше 0
    print(f"{number}...")                                #то отсчет продолжается
    number -= 1
print("Go!")                                             #как только цифра = 0, "запускаем ракету"