fruits = ["apple", "banana"]                #ввели основные данные
print(fruits)
fruits.append("orange")                     #добавили орэнж в конец списка
print(fruits)
fruits.insert(1, "grapes")   #добавили грэйпс в начало
print(fruits)
fruits.remove("banana")                     #удалили банана
print(fruits)
fruits.sort()                               #отсортировали список
print(fruits)
fruits.reverse()                            #отзеркалили список
print(fruits)                               #финальный вывод